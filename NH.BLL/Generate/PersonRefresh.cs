		/// <summary>
		/// 获得数据列表
		/// </summary>
		public static List<NH.Model.PersonRefresh> DataTableToList(DataTable dt)
		{
			List<NH.Model.PersonRefresh> modelList = new List<NH.Model.PersonRefresh>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				NH.Model.PersonRefresh model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new NH.Model.PersonRefresh();					
													if(dt.Rows[n]["Id"].ToString()!="")
				{
					model.Id=int.Parse(dt.Rows[n]["Id"].ToString());
				}
																																if(dt.Rows[n]["MemberID"].ToString()!="")
				{
					model.MemberID=int.Parse(dt.Rows[n]["MemberID"].ToString());
				}
																																if(dt.Rows[n]["RefreshTime"].ToString()!="")
				{
					model.RefreshTime=DateTime.Parse(dt.Rows[n]["RefreshTime"].ToString());
				}
																										
				
					modelList.Add(model);
				}
			}
			return modelList;
		}
