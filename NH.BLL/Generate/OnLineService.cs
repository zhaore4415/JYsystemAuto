        /// <summary>
        /// 得到一个对象实体，从缓存中
        /// </summary>
        public static NH.Model.OnLineService GetModelByCache(int Id)
        {

            string CacheKey = "OnLineServiceModel-" + Id;
            object objModel = Maticsoft.Common.DataCache.GetCache(CacheKey);
            if (objModel == null)
            {
                try
                {
                    objModel = DAL.OnLineService.GetModel(Id);
                    if (objModel != null)
                    {
                        int ModelCache = Maticsoft.Common.ConfigHelper.GetConfigInt("ModelCache");
                        Maticsoft.Common.DataCache.SetCache(CacheKey, objModel, DateTime.Now.AddMinutes(ModelCache), TimeSpan.Zero);
                    }
                }
                catch { }
            }
            return (NH.Model.OnLineService)objModel;
        }
