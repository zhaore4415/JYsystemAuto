        #region 获得数据列表
        /// <summary>
        /// 获得数据列表
        /// </summary>
        public static List<NH.Model.area> DataTableToList(DataTable dt)
        {
            List<NH.Model.area> modelList = new List<NH.Model.area>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                NH.Model.area model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = new NH.Model.area();
                    if (dt.Rows[n]["id"].ToString() != "")
                    {
                        model.id = int.Parse(dt.Rows[n]["id"].ToString());
                    }
                    model.code = dt.Rows[n]["code"].ToString();
                    model.name = dt.Rows[n]["name"].ToString();
                    model.cityId = dt.Rows[n]["cityId"].ToString();


                    modelList.Add(model);
                }
            }
            return modelList;
        }
        #endregion

