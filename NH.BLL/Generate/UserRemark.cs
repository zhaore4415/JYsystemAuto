        #region 获得数据列表
        /// <summary>
        /// 获得数据列表
        /// </summary>
        public static List<NH.Model.UserRemark> DataTableToList(DataTable dt)
        {
            List<NH.Model.UserRemark> modelList = new List<NH.Model.UserRemark>();
            int rowsCount = dt.Rows.Count;
            if (rowsCount > 0)
            {
                NH.Model.UserRemark model;
                for (int n = 0; n < rowsCount; n++)
                {
                    model = new NH.Model.UserRemark();
                    if (dt.Rows[n]["Id"].ToString() != "")
                    {
                        model.Id = int.Parse(dt.Rows[n]["Id"].ToString());
                    }
                    if (dt.Rows[n]["CompanyId"].ToString() != "")
                    {
                        model.CompanyId = int.Parse(dt.Rows[n]["CompanyId"].ToString());
                    }
                    if (dt.Rows[n]["MemberId"].ToString() != "")
                    {
                        model.MemberId = int.Parse(dt.Rows[n]["MemberId"].ToString());
                    }
                    if (dt.Rows[n]["RemarkTypeId"].ToString() != "")
                    {
                        model.RemarkTypeId = int.Parse(dt.Rows[n]["RemarkTypeId"].ToString());
                    }
                    if (dt.Rows[n]["AddTime"].ToString() != "")
                    {
                        model.AddTime = DateTime.Parse(dt.Rows[n]["AddTime"].ToString());
                    }


                    modelList.Add(model);
                }
            }
            return modelList;
        }
        #endregion

