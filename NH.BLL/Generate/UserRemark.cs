        public static NH.Model.UserRemark GetModelByCache(int Id)
        {

            string CacheKey = "UserRemarkModel-" + Id;
            object objModel = Maticsoft.Common.DataCache.GetCache(CacheKey);
            if (objModel == null)
            {
                try
                {
                    objModel = DAL.UserRemark.GetModel(Id);
